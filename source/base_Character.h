#pragma once
#include<string>
#include<iostream>


//	ステータス
struct CharacterStatus
{
	std::string name ;
	int hp ;
	int attack ;
	//	必要なら属性も追加
};

/// <summary>
/// 規模感か小さいので今回はこのような形にします。
/// 本当ならタグは文字列で検索するべきでタグをレジスターに事前に登録するべきなんだろうけど
/// 今回は許してください。
/// </summary>
enum class CharacterTag
{
	none,
	player,
	enemy,
};

class Character
{
public:		
	virtual ~Character() = default;
	virtual void Initialize(size_t id, CharacterStatus status,CharacterTag tag = CharacterTag::none)
	{
		m_isDead = false;
		m_id = id;
		m_tag = tag;
		m_status = status;
	};
	virtual void Draw() 
	{
		std::cout << m_status.name << " | 体力: " << std::to_string(m_status.hp) << std::endl;
	};

	//	殴られる
	void Damage(int attack) 
	{
		if (m_isDead == true)
		{
			return;
		}

		//	防御力を追加したらここで計算
		auto damage = attack;
		m_status.hp -= damage;

		//	死亡判定
		if (m_status.hp <= 0)
		{
			m_isDead = false;
		}
	}

	//	殴る
	int Attack()const
	{
		if (m_status.attack > 0)
		{
			std::cout << m_status.name << "の" << "こうげき！" << std::endl;
			return m_status.attack;
		}

		return 0;
	}

	//	回復
	void Heal(int heal)
	{
		if (m_isDead == true)
		{
			return;
		}

		m_status.hp += heal;

	}

	bool IsDead()const { return m_isDead; }

protected:
	/// <summary>
	/// キャラごとのステータスの構造体
	/// </summary>
	CharacterStatus m_status;

	/// <summary>
	/// 自分の種別
	/// </summary>
	CharacterTag m_tag;

	//	自分が何番のIDかどうか
	size_t m_id = -1;

	/// <summary>
	/// 生存フラグ
	/// </summary>
	bool m_isDead = false;
};